export function dateToString(date) {
    const month = {
        0: 'Janv',
        1: 'Févr',
        2: 'Mars',
        3: 'Avril',
        4: 'Mai',
        5: 'Juin',
        6: 'Juil',
        7: 'Août',
        8: 'Sept',
        9: 'Oct',
        10: 'Nov',
        11: 'Déc'
    }[date.getMonth()];
    return date.getDate().toString() + ' ' + month.toString();
}

export function timeToString(date) {
    const day = {
        0: 'Dimanche',
        1: 'Lundi',
        2: 'Mardi',
        3: 'Mercredi',
        4: 'Jeudi',
        5: 'Vendredi',
        6: 'Samedi'
    }[date.getDay()];

    const hour = date.getHours();
    const minutes =
        date.getMinutes() < 10 ? '0' + date.getMinutes().toString() : date.getMinutes().toString();
    return day.toString() + ' à ' + hour.toString() + ':' + minutes;
}

